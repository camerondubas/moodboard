#[component]
pub fn Icon<I, O, S, M, IV>(style: I, outline: O, solid: S, mini: M) -> impl IntoView
where
    I: Fn() -> IconStyle + 'static,
    O: Fn() -> IV + 'static,
    S: Fn() -> IV + 'static,
    M: Fn() -> IV + 'static,
    IV: IntoView,
{
    view! {
      {
        move || match style() {
          IconStyle::Outline => outline(),
          IconStyle::Solid => solid(),
          IconStyle::Mini => mini(),
        }
      }

    }
}
